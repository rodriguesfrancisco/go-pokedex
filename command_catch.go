func commandCatch(page *pageConfig) error {
	pokemonName := page.Parameters[0]
	if pokemonName == "" {
		err := errors.New("you need to send an location area name to explore")
		fmt.Println(err)
		return err
	}

	fullUrl := "https://pokeapi.co/api/v2/pokemon/" + pokemonName

	data, ok := page.Cache.Get(fullUrl)
	if ok {
		response := PokemonResponse{}
		err := json.Unmarshal(data, &response)
		if err != nil {
			return err
		}

		caught := tryToCatch(response)
		if caught {
			page.Pokedex[response.Name] = response
		}
		return nil
	}

	res, err := http.Get(fullUrl)
	if err != nil {
		return err
	}
	defer res.Body.Close()
	body, err := io.ReadAll(res.Body)
	if err != nil {
		return err
	}

	if res.StatusCode > 299 {
		return fmt.Errorf("call to pokemon details endpoint return status code %d", res.StatusCode)
	}

	page.Cache.Add(fullUrl, body)

	response := PokemonResponse{}
	err = json.Unmarshal(body, &response)
	if err != nil {
		return err
	}

	caught := tryToCatch(response)
	if caught {
		page.Pokedex[response.Name] = response
	}

	return nil
}

func tryToCatch(pokemon PokemonResponse) bool {
	var difficultThreshold int
	if pokemon.BaseExperience <= 120 {
		difficultThreshold = 90 // Easy
	} else if pokemon.BaseExperience > 120 && pokemon.BaseExperience < 200 {
		difficultThreshold = 50 // Medium
	} else {
		difficultThreshold = 10 // Hard
	}

	fmt.Printf("Throwing a Pokeball at %s...\n", pokemon.Name)
	randInt := rand.IntN(100)
	if randInt <= difficultThreshold {
		fmt.Printf("%s was caught!\n", pokemon.Name)
		return true
	}

	fmt.Printf("%s escaped!\n", pokemon.Name)
	return false
}

